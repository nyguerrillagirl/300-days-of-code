var day = 'Sunday';

switch (day) {
    case 'Monday':
        console.log('Work on Coursera courses on Monday');
        break;
    case 'Tuesday':
        console.log('Read a book on Tuesday');
        break;
    case 'Wednesday':
        console.log('Play Age of Empires II on Wednesday');
        break;
    case 'Thursday':
        console.log('Take a Live Course on O\'Reilly Learning on Thursday');
        break;
    case 'Friday':
        console.log('Listen to a podcast on Friday');
        break;
    case 'Saturday':
        console.log('Watch something new on Twitch on Saturday');
        break;
    case 'Sunday':
        console.log('Do nothing on Sunday');
        break;
    default:
        console.log('There is no such day as ' + day);
}