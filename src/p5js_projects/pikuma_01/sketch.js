//////////////////////////////////////////////////////////////////////////
// Vector 2D class (blueprint for all the vector objects)
//////////////////////////////////////////////////////////////////////////

class Vec2 {
  constructor(x, y) {
    this.x = x;
    this.y = y;
  }
  draw() {
    fill("white");
    circle(this.x, this.y, 10);
  }
}

// Create a new object of the type Vec2
let position = new Vec2(10, 20);

//////////////////////////////////////////////////////////////////////////
// Setup function, called once at the beginning
//////////////////////////////////////////////////////////////////////////
function setup() {
  createCanvas(windowWidth, windowHeight);
}

//////////////////////////////////////////////////////////////////////////
// Draw function, called every frame several times per second
//////////////////////////////////////////////////////////////////////////
function draw() {
  background("black");
 
  position.draw();

}
